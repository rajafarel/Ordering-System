    private void jCheckBox13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox13ActionPerformed
       int quantity = Integer.parseInt(jSpinner13.getValue().toString());
        if(quantityIsZero(quantity) && jCheckBox13.isSelected()){
            x++;
            if(x==1){
               menu(); 
            }
            double price = quantity*8.0;
            total += price;
            getTax(total);
            bigTotal();
            for (int i = 0; i < quantity; i++) {
                    item.add("item");
            }
            jTextArea1.setText(jTextArea1.getText()+x+"."+jLabel38.getText()+"\t"+String.format("%.2f", price)+"\n");
        }else{
            jCheckBox13.setSelected(false);
        }
    }//GEN-LAST:event_jCheckBox13ActionPerformed

